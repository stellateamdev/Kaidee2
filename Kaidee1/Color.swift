import Foundation
import UIKit
extension UIColor {
    class func kaideeOrange() -> UIColor {
        
        return UIColor(red: 252/255.0, green: 143/255.0, blue: 60/255.0, alpha: 1)
    }
    class func kaideeBlue() -> UIColor {
        return UIColor(red: 56/255.0, green: 81/255.0, blue: 195/255.0, alpha: 1)
    }
    class func kaideeGreen() -> UIColor {
        return UIColor(red: 62/255.0, green: 200/255.0, blue: 121/255.0, alpha: 1)
    }
    
}
